//Cette fonction, appelée dès le chargement du body, va initialiser les variables utilisées dans le diaporama mais également appeler la fonction ouvrir() au bout d'une courte période (affichage de la pop-up)
function initVar() {
  var diapoRolling = 0;
  var currentImg = 0;
  var nbImg = 3;
  var images = [
    "https://cdn.glitch.com/f85f0200-331a-4de4-b01d-09c0a67f4030%2Fmini.jpg?v=1618409383509",
    "https://cdn.glitch.com/f85f0200-331a-4de4-b01d-09c0a67f4030%2Fmini1.jpg?v=1618409402282",
    "https://cdn.glitch.com/f85f0200-331a-4de4-b01d-09c0a67f4030%2Fmini2.jpg?v=1618409408552"
  ];

  myImg = document.getElementById("myImg");

  setTimeout(ouvrir, 2000);
}
//le bouton de contrôle gauche du diaporama lance cette fonction. La variable "currentImg", réprésentable par un curseur, va se décaler d'une image en arrière dans le tableau. Si l'image est en bout de tableau, le curseur retourne à la fin. L'image du diaporama devient alors celle pointée par le curseur.
function prev() {
  currentImg--;
  if (currentImg < 0) {
    currentImg = nbImg - 1;
  }
  myImg.src = images[currentImg];
}

//le bouton de contrôle droit du diaporama lance cette fonction. La variable "currentImg", réprésentable par un curseur, va se décaler d'une image en avant dans le tableau. Si l'image est en bout de tableau, le curseur retourne au début. L'image du diaporama devient alors celle pointée par le curseur.
function next() {
  currentImg++;
  if (currentImg > nbImg - 1) {
    currentImg = 0;
  }
  myImg.src = images[currentImg];
}
//Le bouton de contrôle central du diaporama lance cette fonction. La variable booléenne "diapoRolling", qui alterne à chaque appel,  permet de détecter si le diaporama est activé ou en pause. S'il est en pause, la fonction "launch()" est appelée. Sinon, le décompte "myVar" est arrêté, empêchant la fonction "launch()" de se renouveler.
function startstop() {
  if (diapoRolling == 0) {
    diapoRolling = 1;
    launch();
  } else {
    clearTimeout(myVar);
    diapoRolling = 0;
  }
}

//Cette fonction se lance si la fonction "startstop()"est appelée et que le diaporama est en pause. Elle s'appelle elle même toutes les 2 secondes et lance la fonction "next()", permettant de faire défiler les images du diaporama.
function launch() {
  myVar = setTimeout(launch, 2000);
  next();
}

//Cette fonction est appelée après l'appel de la fonction "initVar()"(lancée par le chargement du body). Elle rend visible la pop-up.
function ouvrir() {
  document.getElementById("popup").style.display = "block";
}

//Cette fonction se lance quand l'utilisateur appuie sur la croix de la pop-up. Il rend cette dernière invisible.
function fermer() {
  document.getElementById("popup").style.display = "none";
}

